// LOOPS AND CONDITIONAL

// Creating a loop that counts from 1 to 10
for (let i = 1; i <= 10; i++) {
    // Check if the current iteration is 5
    if (i === 5) {
      console.log("Iteration is 5. Exiting the loop.");
      break; // Exit the loop
    }
    
    console.log(i); // Log the current iteration
  }
  